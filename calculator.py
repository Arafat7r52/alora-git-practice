# functions
def add(a, b):
    return sum([a, b]);

def subtract(a, b):
    return a - b

def multiply(a, b):
    return a * b;

def divide(a, b):
    return a / b;
def power(base, exp):
    return base ** exp;

def modulo(a, b):
    return a % b;